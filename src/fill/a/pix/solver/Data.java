package fill.a.pix.solver;

/**
 *
 * @author Kaj75
 */
class Data {
    private int num;
    private int x;
    private int y;

    public Data(int num, int x, int y) {
        this.num = num;
        this.x = x;
        this.y = y;
    }

    public int getNum() {
        return num;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    
}
